export function formatMarkdown(analysis, { model, timestamp } = {}) {
  const ts = timestamp ?? new Date().toISOString();
  const mdl = model ?? 'claude-sonnet-4-6';

  const priorityGroups = { HIGH: [], MEDIUM: [], LOW: [], OTHER: [] };
  for (const test of analysis.recommendedTests ?? []) {
    const group = priorityGroups[test.priority?.toUpperCase()] ? test.priority.toUpperCase() : 'OTHER';
    priorityGroups[group].push(test);
  }

  function renderTestGroup(label, tests) {
    if (tests.length === 0) return '';
    return `### ${label} Priority\n` +
      tests.map(t => `- **${t.name}** (${t.area})\n  → ${t.reason}`).join('\n') + '\n\n';
  }

  const impactedTable = (analysis.impactedAreas ?? []).length > 0
    ? `| Area | Confidence | Reason |\n|------|-----------|--------|\n` +
      (analysis.impactedAreas ?? []).map(a => `| ${a.area} | ${a.confidence} | ${a.reason} |`).join('\n') + '\n'
    : '_No impacted areas identified._\n';

  const gapsSection = (analysis.coverageGaps ?? []).length > 0
    ? 'These scenarios have no existing test case:\n\n' +
      (analysis.coverageGaps ?? []).map((g, i) =>
        `${i + 1}. **${g.description}**\n   Suggested: ${g.suggestedTestCase}`
      ).join('\n\n') + '\n'
    : '_No coverage gaps identified._\n';

  const testCount = analysis.recommendedTests?.length ?? 0;
  const wikiPages = analysis.contextSources?.wikiPagesUsed ?? [];

  return `# Ripple Analysis — ${analysis.ticketKey}

${analysis.summary}

Analyzed: ${ts}

## Risk Level: ${analysis.riskLevel}

${analysis.riskReason}

## Impacted Areas

${impactedTable}
## Recommended Tests (${testCount})

${renderTestGroup('HIGH', priorityGroups.HIGH)}${renderTestGroup('MEDIUM', priorityGroups.MEDIUM)}${renderTestGroup('LOW', priorityGroups.LOW)}${renderTestGroup('OTHER', priorityGroups.OTHER)}
## Coverage Gaps

${gapsSection}
## Context

- Wiki pages referenced: ${wikiPages.length > 0 ? wikiPages.join(', ') : 'none'}
- Test cases evaluated: ${analysis.contextSources?.testCasesEvaluated ?? 0}
- Test cases recommended: ${analysis.contextSources?.testCasesRecommended ?? 0}

---
Generated by Ripple • ${mdl}
`;
}
